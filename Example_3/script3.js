/* 
    indexOf = descobrir o índice de um elemento em uma lista
    length = descobrir quantidade de índices na lista
    push = adicionar elemento a lista 
*/

let nomes = ["Yasmin", "Julya", "Larissa", "Solange", "Heber", "Yuri"];
alert(nomes[3]); // Imprimir conforme o índice desejado

alert(nomes.indexOf("Solange"));

alert(nomes.length);

alert(nomes.push("Mariana"));
